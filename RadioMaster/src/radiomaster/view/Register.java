    /**
     * Pressing button SignIn the first will be checked if all fields are 
     * filled and then it will store the data that Register Model contains.
     * @param evt 
     */
    
    private void btnSignInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignInActionPerformed

        if (!control()) {
            return;
        }

        HttpWrapper wrapper = new HttpWrapper();
        String postdata = "username=" + txtUsername.getText() + "&" + "email=" + txtEmail.getText() + "&" + "password=" + String.valueOf(txtPassword.getPassword());
        byte[] bodyContent = postdata.getBytes();

        wrapper.setURL(REGISTER_URL)
                .setMethod("POST")
                .setOnCompletionListener(this)
                .setBody(bodyContent);

        wrapper.run();

        
    }//GEN-LAST:event_btnSignInActionPerformed

